/**
 * VSCode-style color themes for Empire Dashboard.
 * Each theme defines CSS custom properties that override the base variables.
 * The glass morphism system adapts to each theme's accent color.
 */

export interface ThemeDef {
  id: string;
  name: string;
  label: string;
  /** Short description shown in theme picker */
  description: string;
  /** Theme mode: dark or light */
  mode: "dark" | "light";
  /** Preview swatch colors for the picker UI */
  swatches: [string, string, string, string];
  /** CSS custom property overrides applied to :root */
  vars: Record<string, string>;
  /** Glass morphism overrides */
  glass: {
    bg: string;
    panel: string;
    panelHover: string;
    panelBorder: string;
    panelBorderHover: string;
    deep: string;
    deepBorder: string;
    elevated: string;
    elevatedBorder: string;
  };
  /** Accent glow color for box-shadows */
  glow: string;
  /** Ambient orb colors */
  orbs: [string, string, string];
  /** Scrollbar colors */
  scrollbar: { thumb: string; thumbHover: string };
  /** Selection colors */
  selection: { bg: string; fg: string };
}

